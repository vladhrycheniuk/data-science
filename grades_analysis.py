grades = [67, 89, 45, 100, 76, 88, 92, 58]

# Середнє значення
average = sum(grades) / len(grades)

# Мінімум і максимум
min_grade = min(grades)
max_grade = max(grades)

# Сортування
sorted_grades = sorted(grades)

print("Оцінки:", grades)
print("Середнє значення:", average)
print("Мінімальна оцінка:", min_grade)
print("Максимальна оцінка:", max_grade)
print("Відсортовані оцінки:", sorted_grades)
